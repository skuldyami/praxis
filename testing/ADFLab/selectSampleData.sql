SELECT BancoSangre.ID, 
       BancoSangre.PERSONA_ID, 
       BancoSangre.TIPO_DONACION_ID, 
       BancoSangre.CANTIDAD_EN_ML, 
       BancoSangre.FE_DONACION, 
       BancoSangre.FE_EXPIRACION
FROM  BANCO_SANGRE BancoSangre;

